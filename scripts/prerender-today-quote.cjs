#!/usr/bin/env node
/**
 * Prerender 今日金句 + 底圖到 src/data/_today-quote.generated.ts
 *
 * Why: LandingPage / OverviewTab 只需要「今天那筆」, 不該 lazy-load 整包
 *      dailyQuotes (99.81 KB / 39.60 KB gz). Build-time 算好今天的, runtime
 *      只 import 一筆 quote + 一筆 background.
 *
 * 限制: build 是離散事件, 今天 build 完隔天才會換. 接受.
 *
 * Algorithm: 必須與 src/data/dailyQuotes.ts 的 hashDateStr + getTodayQuote /
 *            getTodayBackground 完全一致, 否則 prerender 結果跟 runtime 取的
 *            隨機分支會錯位.
 */
const fs = require('fs');
const path = require('path');

const SRC = path.resolve(__dirname, '../src/data/dailyQuotes.ts');
const OUT = path.resolve(__dirname, '../src/data/_today-quote.generated.ts');

function readSource() {
  return fs.readFileSync(SRC, 'utf-8');
}

// 抓 export const NAME ... = [ ... ]; 整段陣列字面值
function extractArrayLiteral(source, name) {
  const startRe = new RegExp(`export const ${name}[^=]*=\\s*\\[`);
  const m = source.match(startRe);
  if (!m) throw new Error(`extractArrayLiteral: cannot find ${name}`);
  const startIdx = m.index + m[0].length - 1; // 指向 '['
  let depth = 0;
  let inStr = null;
  let escape = false;
  for (let i = startIdx; i < source.length; i++) {
    const ch = source[i];
    if (inStr) {
      if (escape) { escape = false; continue; }
      if (ch === '\\') { escape = true; continue; }
      if (ch === inStr) { inStr = null; }
      continue;
    }
    if (ch === '"' || ch === "'" || ch === '`') { inStr = ch; continue; }
    if (ch === '[') depth++;
    else if (ch === ']') {
      depth--;
      if (depth === 0) return source.slice(startIdx, i + 1);
    }
  }
  throw new Error(`extractArrayLiteral: unbalanced brackets in ${name}`);
}

// 移除註解, 但要避開 string literal (避免 URL 內 "//" 被吃掉)
function stripComments(src) {
  let out = '';
  let inStr = null;
  let escape = false;
  for (let i = 0; i < src.length; i++) {
    const ch = src[i];
    const next = src[i + 1];
    if (inStr) {
      out += ch;
      if (escape) { escape = false; continue; }
      if (ch === '\\') { escape = true; continue; }
      if (ch === inStr) inStr = null;
      continue;
    }
    if (ch === '"' || ch === "'" || ch === '`') {
      inStr = ch;
      out += ch;
      continue;
    }
    if (ch === '/' && next === '/') {
      while (i < src.length && src[i] !== '\n') i++;
      if (i < src.length) out += '\n';
      continue;
    }
    if (ch === '/' && next === '*') {
      i += 2;
      while (i < src.length && !(src[i] === '*' && src[i + 1] === '/')) i++;
      i++;
      continue;
    }
    out += ch;
  }
  return out;
}

// 把 array literal 轉成 JS 物件. 用 Function 直接 eval (build-time script,
// 信任輸入). 比 JSON.parse 更寬鬆 (允許 single quote / trailing comma / 物件鍵不加引號).
function evalArrayLiteral(literal) {
  const cleaned = stripComments(literal);
  // eslint-disable-next-line no-new-func
  return new Function(`return (${cleaned});`)();
}

// 必須 byte-for-byte 對齊 dailyQuotes.ts 的 hashDateStr
function hashDateStr(dateStr) {
  let hash = 5381;
  for (let i = 0; i < dateStr.length; i++) {
    hash = ((hash << 5) + hash + dateStr.charCodeAt(i)) & 0x7fffffff;
  }
  return hash;
}

function todayDateStr() {
  // 跟 runtime new Date().toISOString().split('T')[0] 一致 (UTC)
  return new Date().toISOString().split('T')[0];
}

function serializeQuote(q) {
  return `{ text: ${JSON.stringify(q.text)} }`;
}

function serializeBackground(bg) {
  return `{ id: ${JSON.stringify(bg.id)}, imageUrl: ${JSON.stringify(bg.imageUrl)}, fallbackGradient: ${JSON.stringify(bg.fallbackGradient)} }`;
}

function main() {
  const source = readSource();
  const quotes = evalArrayLiteral(extractArrayLiteral(source, 'dailyQuotes'));
  const backgrounds = evalArrayLiteral(extractArrayLiteral(source, 'storyBackgrounds'));

  if (!Array.isArray(quotes) || quotes.length === 0) {
    throw new Error('prerender: dailyQuotes empty');
  }
  if (!Array.isArray(backgrounds) || backgrounds.length === 0) {
    throw new Error('prerender: storyBackgrounds empty');
  }

  const dateStr = todayDateStr();
  const h = hashDateStr(dateStr);
  const quoteIdx = h % quotes.length;
  const bgIdx = h % backgrounds.length;
  const quote = quotes[quoteIdx];
  const bg = backgrounds[bgIdx];

  const banner =
    `// AUTO-GENERATED by scripts/prerender-today-quote.cjs — DO NOT EDIT.\n` +
    `// Built ${new Date().toISOString()} for date ${dateStr}.\n` +
    `// Source: src/data/dailyQuotes.ts (${quotes.length} quotes / ${backgrounds.length} backgrounds).\n`;

  const body =
    `import type { DailyQuote } from './dailyQuotes';\n` +
    `\n` +
    `export interface PrerenderedBackground {\n` +
    `  id: number;\n` +
    `  imageUrl: string;\n` +
    `  fallbackGradient: string;\n` +
    `}\n` +
    `\n` +
    `export const TODAY_DATE_STR: string = ${JSON.stringify(dateStr)};\n` +
    `\n` +
    `export const todayQuote: DailyQuote = ${serializeQuote(quote)};\n` +
    `\n` +
    `export const todayBackground: PrerenderedBackground = ${serializeBackground(bg)};\n`;

  fs.writeFileSync(OUT, banner + body, 'utf-8');
  console.log(
    `[prerender-today-quote] date=${dateStr} quote#${quoteIdx + 1}/${quotes.length} bg#${bg.id} -> ${path.relative(process.cwd(), OUT)}`
  );
}

main();
